    body_html = (
        _logo()
        + _p(_s(L, 'receipt_hi').format(name=safe_name))
        + _p(_s(L, 'receipt_body').format(event=safe_event))
        + group_code_section
        + _p(_s(L, 'receipt_closing'), muted=True)
    )

    html = _shell(
        title=subject,
        preheader=_s(L, 'receipt_preheader'),
        body_html=body_html,
        lang=L,
    )

    text_parts = [
        _html_to_text(_s(L, 'receipt_hi').format(name=first_name)),
        "",
        _html_to_text(_s(L, 'receipt_body').format(event=event_name)),
        "",
    ]
